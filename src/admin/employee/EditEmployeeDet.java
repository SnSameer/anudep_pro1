    void getEmployee(){
        model = (DefaultTableModel) jTable.getModel();
        String name,email;
        int srno = 0;
        try{
            ResultSet rs = dbconnection.Db_Operations.showAllEmployee();
            if(rs!=null){
                while(rs.next()){
                    srno += 1;
                    name = rs.getString("name");
                    email = rs.getString("email");
                    
                    row=new Object[]{srno,name,email};
                    model.addRow(row);
//                    System.out.println(name+" : "+email);
                }
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }

