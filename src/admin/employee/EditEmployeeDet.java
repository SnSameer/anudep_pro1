    private void jTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMouseClicked
        // TODO add your handling code here:
        int row = jTable.getSelectedRow();
        String email = (String)jTable.getValueAt(row, 2);
        ResultSet rs = dbconnection.Db_Operations.getSelectedEmployee(email);
        try{
            if(rs!=null){
                if(rs.next()){
                    jTextField1.setText(rs.getString("name"));
                    jTextField2.setText(rs.getString("email"));
                    jTextField3.setText(rs.getString("password"));
                    jTextField4.setText(rs.getString("phone_no"));
                    String gender = rs.getString("gender");
                    if(gender.equals("M")){
                        jRadioButton1.setSelected(true);
                    }
                else{
                      jRadioButton2.setSelected(true);
                    }
                }
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
        System.out.println(email);
    }//GEN-LAST:event_jTableMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        String email = jTextField2.getText();
        String pass = jTextField3.getText();
        String phone = jTextField4.getText();
        String gender = "";
        if(jRadioButton1.isSelected()){
            gender = "M";
        }else{
            gender = "F";
        }
        int i = dbconnection.Db_Operations.UpdateEmployeeDetails(name, email, phone, gender, pass);
        if(i>0) 
        {    
            JOptionPane.showMessageDialog(this, "Values updated Successfully");
            new admin.main.AdminPanel().setVisible(true);
        }else{
            JOptionPane.showMessageDialog(this, "An Error Occured","Update Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

